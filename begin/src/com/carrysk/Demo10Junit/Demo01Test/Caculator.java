package com.carrysk.Demo10Junit.Demo01Test;

public class Caculator {

    /**
     * 加法
     *
     * @param num1
     * @param num2
     * @return
     */
    int add(int num1, int num2) {
        return num1 + num2;
    }

    /**
     * 减法
     *
     * @param num1
     * @param num2
     * @return
     */
    int sub(int num1, int num2) {
        return num1 - num2;
    }
}
